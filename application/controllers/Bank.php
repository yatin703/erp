  public function save(){
    
    $this->form_validation->set_rules('bank_code','Bank code' ,'required|xss_clean|max_length[50]|strtoupper|is_unique[bank_master.bank_code]');
    $this->form_validation->set_rules('bank_name','Bank Name' ,'required|xss_clean|max_length[50]|strtoupper');
    $this->form_validation->set_rules('bank_address','Bank Address' ,'xss_clean|max_length[200]|strtoupper');
    $this->form_validation->set_rules('od_limit_ammount','Credit Amount' ,'xss_clean|max_length[20]|numeric');
  

    if($this->form_validation->run()==FALSE){

      $data['page_name']='setup';

      $data['module']=$this->common_model->select_active_module($this->session->userdata['logged_in']['user_id'],$this->session->userdata['logged_in']['company_id']);

      
      $this->load->view('Home/header');

      $this->load->view('Home/nav',$data);

      $this->load->view('Home/subnav');

      $this->load->view(ucwords($this->router->fetch_class()).'/active-title');

      $this->load->view(ucwords($this->router->fetch_class()).'/create-form',$data);

      $this->load->view('Home/footer');

    }
    else{

          $max_pkey=0;
          $result=$this->common_model->select_max_pkey_noncompany('bank_master','bank_id'); 
          
          foreach($result as $row){

            $max_pkey=$row->bank_id;
          }

          $bank_id=$max_pkey+1;
           
          $data=array('bank_id'=>$bank_id,
                       'bank_code'=>$this->input->post('bank_code'),
                       'bank_name'=>$this->input->post('bank_name'),
                       'bank_address'=>$this->input->post('bank_address'),
                       'od_limit_amount'=>$this->input->post('od_limit_amount'),
                       'archive'=>'0'
                       );


          $result=$this->common_model->save('bank_master',$data);

          if($result==1){

            $data['note']='Save Transaction Completed';

            header("refresh:1;url=".base_url()."index.php/".$this->router->fetch_class());

            $data['page_name']='setup';

            $data['module']=$this->common_model->select_active_module($this->session->userdata['logged_in']['user_id'],$this->session->userdata['logged_in']['company_id']);

            
            $this->load->view('Home/header');

            $this->load->view('Home/nav',$data);

            $this->load->view('Home/subnav');

            $this->load->view(ucwords($this->router->fetch_class()).'/active-title');

            $this->load->view(ucwords($this->router->fetch_class()).'/create-form',$data);

            $this->load->view('Home/footer');

             
          }
          else{

            $data['note']='Error in Save Transaction';

            $data['page_name']='setup';

            $data['module']=$this->common_model->select_active_module($this->session->userdata['logged_in']['user_id'],$this->session->userdata['logged_in']['company_id']);

            $this->load->view('Home/header');

            $this->load->view('Home/nav',$data);

            $this->load->view('Home/subnav');

            $this->load->view('Error/error-title',$data);

            $this->load->view('Home/footer');

        }

    }


    
